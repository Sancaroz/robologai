function filterCompanies() {
  const query = normalizeSearch(searchInput.value.trim());
  renderCompanyDatabase(searchInput.value.trim());
  if (!companyCards.length) {
    filterMarketRows(query);
    return;
  }
  const category = categoryFilter?.value || "all";
  const subcategory = subcategoryFilter?.value || "all";
  const country = countryFilter?.value || "all";
  let visibleCount = 0;

  companyCards.forEach((card) => {
    const haystack = normalizeSearch(`${card.dataset.company || ""} ${card.textContent}`);
    const matchesSearch = !query || haystack.includes(query);
    const matchesCategory = category === "all" || card.dataset.category === category;
    const matchesSubcategory = subcategory === "all" || card.dataset.subcategory === subcategory;
    const matchesCountry = country === "all" || card.dataset.country === country;
    const isExtra = card.dataset.extra === "true";
    const isVisible = matchesSearch && matchesCategory && matchesSubcategory && matchesCountry && (extraCompaniesVisible || !isExtra);
    card.hidden = !isVisible;
    if (isVisible) visibleCount += 1;
  });

  if (noResults) noResults.hidden = visibleCount > 0;
  const hasActiveFilter = Boolean(query) || category !== "all" || subcategory !== "all" || country !== "all";
  if (profilePanel) profilePanel.hidden = hasActiveFilter;
  if (loadMore) loadMore.hidden = hasActiveFilter || extraCompaniesVisible;
}

function filterMarketRows(query = normalizeSearch(searchInput?.value?.trim() || "")) {
  if (!marketRows.length) return;
  marketRows.forEach((row) => {
    const haystack = normalizeSearch(row.dataset.search || row.textContent);
    const type = normalizeSearch(row.textContent);
    const matchesSearch = !query || haystack.includes(query);
    const matchesFilter =
      activeMarketFilter === "all" ||
      (activeMarketFilter === "public" && type.includes("public")) ||
      (activeMarketFilter === "private" && type.includes("private")) ||
      (activeMarketFilter === "humanoid" && type.includes("humanoid")) ||
      (activeMarketFilter === "ai" && type.includes("ai"));
    row.hidden = !(matchesSearch && matchesFilter);
  });
}

function sortCompanies() {
  if (!sortFilter || !companiesSection) return;
  const sortedCards = [...companyCards];
  if (sortFilter.value === "new") {
    sortedCards.sort((a, b) => Number(b.dataset.founded) - Number(a.dataset.founded));
  } else if (sortFilter.value === "founded") {
    sortedCards.sort((a, b) => Number(a.dataset.founded) - Number(b.dataset.founded));
  }
  sortedCards.forEach((card) => companiesSection.appendChild(card));
  filterCompanies();
}

function initNewsletterScroll() {
  const newsletter = document.querySelector("#bulten");
  const alignNewsletter = () => {
    if (!newsletter) return;
    const header = document.querySelector(".site-header");
    const headerOffset = header ? header.getBoundingClientRect().height + 28 : 96;
    const targetTop = newsletter.getBoundingClientRect().top + window.pageYOffset - headerOffset;
    window.scrollTo({ top: Math.max(0, targetTop), behavior: "auto" });
  };
  if (newsletter && window.location.hash === "#bulten") {
    [0, 120, 420, 900].forEach((delay) => window.setTimeout(alignNewsletter, delay));
  }
  if (!newsletterCtas.length) return;
  newsletterCtas.forEach((link) => {
    link.addEventListener("click", (event) => {
      const isSamePage = link.getAttribute("href") === "#bulten" || window.location.pathname.endsWith("index.html") || window.location.pathname === "/";
      if (!isSamePage || !newsletter) return;
      event.preventDefault();
      history.replaceState(null, "", "#bulten");
      alignNewsletter();
    });
  });
}

function initNewsletterSignup() {
  const form = document.querySelector("[data-newsletter-form]");
  if (!form) return;
  const emailInput = form.querySelector('input[name="email"]');
  const submitButton = form.querySelector('button[type="submit"]');
  const message = document.querySelector("[data-newsletter-message]");
  const fallbackLink = document.querySelector(".newsletter-fallback-link");

  const setMessage = (text, state = "") => {
    if (!message) return;
    message.textContent = text;
    message.dataset.state = state;
  };

  form.addEventListener("submit", async (event) => {
    event.preventDefault();
    const email = emailInput?.value.trim();
    if (!email) {
      setMessage("Please enter your email.", "error");
      return;
    }

    submitButton.disabled = true;
    setMessage("Sending your signup to RoboLogAI Intel Feed...", "loading");

    try {
      const response = await fetch("/api/subscribe", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ email })
      });

      if (!response.ok) throw new Error("subscribe_failed");
      setMessage("You are on the list. Welcome to RoboLogAI Intel Feed.", "success");
      form.reset();
      fallbackLink?.classList.remove("is-visible");
    } catch (error) {
      setMessage("Direct signup is not live yet. Use the backup beehiiv link for now.", "error");
      fallbackLink?.classList.add("is-visible");
    } finally {
      submitButton.disabled = false;
    }
  });
}

