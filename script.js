async function loadRobotDatabase() {
  try {
    const response = await fetch("data/robots.json", { cache: "no-store" });
    if (!response.ok) throw new Error("Robot database could not be loaded.");
    robotDatabase = await response.json();
  } catch (error) {
    robotDatabase = [];
  }
  renderRobotCatalog();
  renderRobotCompare();
  renderDatabaseInsights();
}

function renderCompanyDatabase(query = "") {
  if (!companyDatabaseResults || !companyDatabaseStatus) return;
  const normalizedQuery = normalizeSearch(query.trim());

  if (normalizedQuery.length < 2) {
    companyDatabaseResults.innerHTML = "";
    companyDatabaseStatus.textContent = `${companyDatabase.length || fallbackCompanyDatabase.length}+ indexed robotics and AI entities. Type at least 2 letters to search the database.`;
    return;
  }

  const terms = normalizedQuery.split(/\s+/).filter(Boolean);
  const matches = companyDatabase
    .map((company) => ({ company, text: companySearchText(company) }))
    .filter(({ text }) => terms.every((term) => text.includes(term)))
    .slice(0, 36)
    .map(({ company }) => company);

  companyDatabaseStatus.textContent = matches.length
    ? `${matches.length} result${matches.length === 1 ? "" : "s"} shown for "${query}".`
    : `No database result found for "${query}". Try a company, robot, country, ticker, or category.`;

  companyDatabaseResults.innerHTML = matches.map((company) => {
    const tags = [company.type, company.country, company.ticker].filter(Boolean).slice(0, 3);
    return `
      <article class="database-result-card">
        <div class="database-tags">${tags.map((tag) => `<span>${escapeHtml(tag)}</span>`).join("")}</div>
        <strong>${escapeHtml(company.name)}</strong>
        <p>${escapeHtml(company.category)}</p>
        <small>${escapeHtml(company.robot || "Robotics / AI activity")}</small>
        <a href="${escapeHtml(company.website)}" target="_blank" rel="noopener noreferrer">Official website →</a>
      </article>
    `;
  }).join("");
}

