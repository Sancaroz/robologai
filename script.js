function normalizeSearch(value) {
  return value
    .toLocaleLowerCase("tr")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "");
}

function companySearchText(company) {
  return normalizeSearch([
    company.name,
    company.category,
    company.country,
    company.type,
    company.ticker,
    company.robot,
    company.website,
    ...(company.keywords || [])
  ].filter(Boolean).join(" "));
}

function escapeHtml(value = "") {
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function topEntry(items, getKey) {
  const counts = new Map();
  items.forEach((item) => {
    const key = getKey(item);
    if (!key) return;
    counts.set(key, (counts.get(key) || 0) + 1);
  });
  return [...counts.entries()].sort((a, b) => b[1] - a[1])[0] || ["", 0];
}

function broadCountry(country = "") {
  if (country.includes("China")) return "China";
  if (country.includes("USA")) return "USA";
  if (country.includes("Japan")) return "Japan";
  if (country.includes("United Kingdom")) return "United Kingdom";
  if (country.includes("Canada")) return "Canada";
  if (country.includes("Switzerland")) return "Switzerland";
  if (country.includes("Germany")) return "Germany";
  if (country.includes("South Korea")) return "South Korea";
  return country.split("/")[0].trim();
}

function renderDatabaseInsights() {
  const companies = companyDatabase.length ? companyDatabase : fallbackCompanyDatabase;
  const countries = new Set(companies.map((company) => broadCountry(company.country)).filter(Boolean));
  const [topCountry, topCountryCount] = topEntry(companies, (company) => broadCountry(company.country));
  const privateCount = companies.filter((company) => normalizeSearch(company.type).includes("private")).length;

  if (statCompanies) statCompanies.textContent = `${companies.length}+`;
  if (statCountries) statCountries.textContent = `${countries.size}+`;
  if (statRobots) statRobots.textContent = robotDatabase.length ? `${robotDatabase.length}` : "16";

  if (!databaseInsights) return;
  databaseInsights.innerHTML = [
    [`${companies.length}+`, "Company profiles"],
    [topCountry ? `${topCountry} · ${topCountryCount}` : "Global", "Top coverage"],
    [`${privateCount}`, "Private builders"],
    [robotDatabase.length ? `${robotDatabase.length}` : "16", "Robot profiles"]
  ].map(([value, label]) => `<article><strong>${escapeHtml(value)}</strong><span>${escapeHtml(label)}</span></article>`).join("");
}

function robotSearchText(robot) {
  return normalizeSearch([
    robot.name,
    robot.company,
    robot.category,
    robot.country,
    robot.status,
    robot.availability,
    robot.price,
    robot.useCase,
    ...(robot.keywords || [])
  ].filter(Boolean).join(" "));
}

function initials(value = "") {
  const letters = value
    .split(/\s+/)
    .map((part) => (part.match(/[A-Za-z]/) || [""])[0])
    .filter(Boolean)
    .join("")
    .toUpperCase();
  return letters.length >= 2 ? letters.slice(0, 2) : value.replace(/[^A-Za-z0-9]/g, "").slice(0, 2).toUpperCase();
}

function meter(value = 1, max = 5) {
  const filled = Math.max(0, Math.min(max, Number(value) || 0));
  return `<span class="robot-meter" style="--score:${filled / max}"><i></i></span>`;
}

