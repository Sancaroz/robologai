function updateThemeLabel(isLight) {
  if (themeIcon) themeIcon.textContent = isLight ? "☀" : "☾";
  const lightLabel = currentLanguage === "en" ? "Switch to dark theme" : "Koyu temaya geç";
  const darkLabel = currentLanguage === "en" ? "Switch to light theme" : "Açık temaya geç";
  themeToggle?.setAttribute("aria-label", isLight ? lightLabel : darkLabel);
  themeToggle?.setAttribute("title", isLight ? lightLabel : darkLabel);
}

function setTheme(theme) {
  const isLight = theme === "light";
  document.body.classList.toggle("light-theme", isLight);
  updateThemeLabel(isLight);
  localStorage.setItem("robologai-theme", isLight ? "light" : "dark");
}

themeToggle?.addEventListener("click", () => {
  const nextTheme = document.body.classList.contains("light-theme") ? "dark" : "light";
  setTheme(nextTheme);
});

